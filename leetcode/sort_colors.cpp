class Solution {
public:
    void sortColors(vector<int>& nums) {
        int l=0, r=nums.size()-1, temp;
        for(int i=l; i<=r;){
            if(nums[i]==0){
                temp = nums[l];
                nums[l] = nums[i];
                nums[i] = temp;
                ++l;
                ++i;
            }
            if(nums[i]==2){
                temp = nums[r];
                nums[r] = nums[i];
                nums[i] = temp;
                --r;
            }
            else ++i;
        }
    }
};
