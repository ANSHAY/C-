class Solution {
public:
    int minPathSum(vector<vector<int>>& grid) {
        if(grid.empty() || grid[0].empty()) return 0;
        int r = grid.size();
        int c = grid[0].size();
        for(int i=r-2; i>=0; --i){
            grid[i][c-1] += grid[i+1][c-1];
        }
        for(int j=c-2; j>=0; --j){
            grid[r-1][j] += grid[r-1][j+1];
        }
        for(int i=r-2; i>=0; --i){
            for(int j=c-2; j>=0; --j){
                grid[i][j] += min(grid[i+1][j], grid[i][j+1]);
            }
        }
        return grid[0][0];
    }
};
